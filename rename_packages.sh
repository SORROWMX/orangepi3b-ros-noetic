#!/bin/bash

# üîÑ –°–∫—Ä–∏–ø—Ç –ø–µ—Ä–µ–∏–º–µ–Ω–æ–≤–∞–Ω–∏—è –ø–∞–∫–µ—Ç–æ–≤ –¥–ª—è Debian Bookworm
# –ü–µ—Ä–µ–∏–º–µ–Ω–æ–≤—ã–≤–∞–µ—Ç –ø–∞–∫–µ—Ç—ã —Å -0focal –Ω–∞ -0bookworm

echo "üîÑ –ü–µ—Ä–µ–∏–º–µ–Ω–æ–≤–∞–Ω–∏–µ –ø–∞–∫–µ—Ç–æ–≤ –¥–ª—è Debian Bookworm..."

